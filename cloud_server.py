SEASON_START_MONTH = 3
TFS = "☆"
TRS = "§"


def db_conn():
    return psycopg.connect(DATABASE_URL, row_factory=dict_row)


def parse_float_safe(v, default=0.0):
    try:
        return float(str(v).replace(",", "").replace("시간", "").replace("원", "").strip())
    except Exception:
        return default


def compute_wage_from_detail(detail_str):
    if not detail_str:
        return 0
    total = 0
    for part in str(detail_str).split(";"):
        if not part.strip():
            continue
        try:
            _, cnt, pay, _ = part.split("|")
            total += int(float(pay)) * int(float(cnt))
        except Exception:
            pass
    return total


def parse_task_list(text):
    if not text or not str(text).strip():
        return []
    result = []
    for rec in str(text).split(TRS):
        rec = rec.strip()
        if not rec:
            continue
        p = rec.split(TFS)
        while len(p) < 12:
            p.append("")
        result.append({
            "날짜": p[0], "종료날짜": p[1], "작물": p[2], "작업내용": p[3],
            "시작시간": p[4], "종료시간": p[5], "작업시간": p[6], "사용기계": p[7],
            "사용자재": p[8], "병충해": p[9], "인력내역": p[10], "비고": p[11],
        })
    return result


def serialize_task_list(task_list):
    recs = []
    for t in task_list:
        recs.append(TFS.join([
            str(t.get("날짜", "")), str(t.get("종료날짜", "")), str(t.get("작물", "")),
            str(t.get("작업내용", "")), str(t.get("시작시간", "")), str(t.get("종료시간", "")),
            str(t.get("작업시간", "")), str(t.get("사용기계", "")), str(t.get("사용자재", "")),
            str(t.get("병충해", "")), str(t.get("인력내역", "")), str(t.get("비고", "")),
        ]))
    return TRS.join(recs)


def ensure_default_options(cur):
    defaults = {
        "옵션_날씨": ["맑음", "흐림", "비", "눈", "바람", "기타"],
        "옵션_작물": ["한라봉", "천혜향", "유라조생", "극조생"],
        "옵션_작업내용": ["물관리", "방제작업", "전정", "비료주기", "수확", "수리/보수", "기타"],
        "옵션_기계": ["수동방제기", "양수기", "SS기", "예초기", "기타", "사용안함"],
        "옵션_단위": ["개", "병", "통", "포", "kg", "g", "L", "ml", "말"],
    }
    for tbl, items in defaults.items():
        for item in items:
            cur.execute(f'INSERT INTO "{tbl}" ("항목") VALUES (%s) ON CONFLICT ("항목") DO NOTHING', (item,))


