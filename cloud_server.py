@app.route("/api/works", methods=["POST"])
def api_add_work():
    data = request.json or {}
    task_list = data.get("task_items", [])
    if not task_list:
        return jsonify({"ok": False, "msg": "작업이 없습니다"}), 400

    task_list_str = serialize_task_list(task_list)
    all_crops, all_task_names, dates, end_dates, all_wages = [], [], [], [], []
    mat_agg, all_pests = {}, []

    for t in task_list:
        for c in str(t.get("작물", "")).split(","):
            c = c.strip()
            if c and c not in all_crops:
                all_crops.append(c)
        if t.get("작업내용"):
            all_task_names.append(t["작업내용"])
        try:
            dates.append(date.fromisoformat(t.get("날짜", "")))
        except Exception:
            pass
        try:
            end_dates.append(date.fromisoformat(t.get("종료날짜", "") or t.get("날짜", "")))
        except Exception:
            pass
        for part in str(t.get("사용자재", "")).split(";"):
            if not part.strip():
                continue
            pp = part.split("|")
            if len(pp) == 3:
                n, q, u = pp[0], parse_float_safe(pp[1]), pp[2]
                if n:
                    mat_agg[n] = (mat_agg[n][0] + q, u) if n in mat_agg else (q, u)
        for p in str(t.get("병충해", "")).split(","):
            p = p.strip()
            if p and p not in all_pests:
                all_pests.append(p)
        for part in str(t.get("인력내역", "")).split(";"):
            if part.strip():
                all_wages.append(part.strip())

    rep_start = min(dates).isoformat() if dates else date.today().isoformat()
    rep_end = max(end_dates).isoformat() if end_dates else rep_start
    rep_mats = ";".join(f"{n}|{q}|{u}" for n, (q, u) in mat_agg.items())
    rep_wage = ";".join(all_wages)
    now_str = datetime.now().isoformat(timespec="seconds")

    conn = db_conn()
    cur = conn.cursor()
    for name, (qty, _) in mat_agg.items():
        cur.execute('UPDATE "자재" SET "재고" = COALESCE("재고",0) - %s WHERE "자재명"=%s', (qty, name))

    cur.execute("""
        INSERT INTO "작업일지" (
            "날짜","종료날짜","날씨","작물","작업내용","인건비",
            "남자수","남자단가","여자수","여자단가","기타수","기타단가",
            "시작시간","종료시간","작업시간","사용기계","사용자재","적용병충해","비고",
            "생성시각","수정시각","인력내역","작업목록"
        )
        VALUES (%s,%s,%s,%s,%s,%s,%s,%s,%s,%s,%s,%s,%s,%s,%s,%s,%s,%s,%s,%s,%s,%s,%s)
    """, (
        rep_start, rep_end, data.get("날씨", ""), ",".join(all_crops), ", ".join(all_task_names), compute_wage_from_detail(rep_wage),
        0, 0, 0, 0, 0, 0,
        task_list[0].get("시작시간", ""),
        task_list[-1].get("종료시간", ""),
        sum(parse_float_safe(t.get("작업시간", "0")) for t in task_list),
        ", ".join(dict.fromkeys(t.get("사용기계", "") for t in task_list if t.get("사용기계"))),
        rep_mats, ",".join(all_pests), "", now_str, now_str, rep_wage, task_list_str
    ))
    conn.commit()
    cur.close()
    conn.close()
    return jsonify({"ok": True})


