@app.route("/api/stats")
def api_stats():
    y = int(request.args.get("year", date.today().year)); m = int(request.args.get("month", date.today().month))
    import calendar as _cal
    ms = date(y, m, 1).isoformat(); me = date(y, m, _cal.monthrange(y, m)[1]).isoformat()
    if m >= SEASON_START_MONTH:
        ss = date(y, SEASON_START_MONTH, 1).isoformat(); se = (date(y + 1, SEASON_START_MONTH, 1) - timedelta(days=1)).isoformat()
    else:
        ss = date(y - 1, SEASON_START_MONTH, 1).isoformat(); se = (date(y, SEASON_START_MONTH, 1) - timedelta(days=1)).isoformat()
    conn = db_conn(); cur = conn.cursor()
    def calc(s, e):
        cur.execute('SELECT COALESCE(SUM("인건비"),0) AS "합계", COUNT(*) AS "건수" FROM "작업일지" WHERE "날짜" BETWEEN %s AND %s', (s, e))
        r = cur.fetchone() or {}
        return {"인건비": r.get("합계", 0) or 0, "건수": r.get("건수", 0) or 0}
    month_stat = calc(ms, me); season_stat = calc(ss, se)
    cur.close(); conn.close()
    return jsonify({"당월": month_stat, "시즌": season_stat})


@app.route("/api/calendar/<int:year>/<int:month>")
def api_calendar(year, month):
    import calendar as _cal
    month_start = date(year, month, 1); last_day = _cal.monthrange(year, month)[1]; month_end = date(year, month, last_day)
    ms, me = month_start.isoformat(), month_end.isoformat()
    conn = db_conn(); cur = conn.cursor()
    cur.execute('''SELECT "번호","날짜","종료날짜","작물","작업내용","날씨","작업시간",COALESCE("작업목록",'') AS "작업목록", "적용병충해","사용자재","인력내역","업체명","자재비","수리및보수비","총금액","현금결제액","계좌이체액","카드결제액","결제정보" FROM "작업일지" WHERE "날짜" <= %s AND COALESCE("종료날짜","날짜") >= %s ORDER BY "날짜","번호" ''', (me, ms))
